const alphabetButtons = document.getElementById('alphabet-buttons');
const wordsList = document.getElementById('words');
const searchBar = document.getElementById('search-bar');

// Populate alphabet buttons
Object.keys(dictionary).forEach(letter => {
    const button = document.createElement('button');
    button.textContent = letter;
    button.addEventListener('click', () => displayWords(letter));
    alphabetButtons.appendChild(button);
  });
  
  // Display words for a specific letter
  function displayWords(letter) {
    wordsList.innerHTML = '';
    dictionary[letter].forEach(entry => {
      const li = document.createElement('li');
      li.textContent = `${entry.word}: ${entry.definition}`;
      wordsList.appendChild(li);
    });
  }
  
  // Search functionality
  searchBar.addEventListener('input', () => {
    const query = searchBar.value.trim().toLowerCase();
    wordsList.innerHTML = '';
  
    if (query === '') return;  // If search bar is empty, don't show any results
  
    let found = false;
    Object.values(dictionary).forEach(entries => {
      entries.forEach(entry => {
        if (entry.word.toLowerCase().includes(query)) {
          const li = document.createElement('li');
          li.textContent = `${entry.word}: ${entry.definition}`;
          wordsList.appendChild(li);
          found = true;
        }
      });
    });
  
    if (!found) {
      const li = document.createElement('li');
      li.textContent = "No matching words found.";
      wordsList.appendChild(li);
    }
  });